package stepik.basiccourse.all.inheritance34;

public class ComplexNum extends ComplexNumber {

    public ComplexNum(double re, double im) {
        super(re, im);
    }

    @Override
    double getRe() {
        return super.getRe();
    }

    @Override
    public double getIm() {
        return super.getIm();
    }
}
